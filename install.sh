auto_detect() {
  for d in "$HOME" /var/www /usr/share/nginx/html /usr/local/www; do
    found=$(find "$d" -maxdepth 3 -name robots.txt -type f 2>/dev/null | head -1)
    if [ -n "$found" ]; then
      echo "$found"
      return 0
    fi
  done
  return 1
}

